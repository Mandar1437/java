public class Array2D_Enhanced 
{
    public static void main(String[] args) 
    {
        int a[][] = {
            {100,200},
            {300,400},
        };

        for(int arr[]:a)
        {
            for(int x:arr)
            {
                System.out.print(x +" ");
            }
            System.out.println();
        }
        
    }
    
}
