public class Downcasting_datatypes 
{
    
        public static void main(String[] args)
        {
            long l1 = 1021;
            int i1 = (int) l1;
            System.out.println(i1);


            //Example 2
            double d2 = 10.65;
            float f2 = (float)d2;
            System.out.println(f2);


            // Example 3

            double d3 = 10.5;
            int i2 = (int)d3;
            System.out.println(i2);
        }
    
    
}
