public class While1 
{
        public static void main(String[] args) 
        {
            int i = 0;
            while (i<11) 
            {
              if(i%2 ==0)
              {
                System.out.println("The numer is even :" + i );

              } 
              else
              {
                System.out.println("The number is odd :" + i);
              }  
              i++;
            }
            
        }
        
    
}
